package com.exercises.topic_five;

public class Car {

  String name;
  String model;
  int maxSpeed;
  int numDoors;
  public Car(){
    };

  public Car(String name, String model, int maxSpeed, int numDoors) {
    this.name = name;
    this.model = model;
    this.maxSpeed = maxSpeed;
    this.numDoors = numDoors;
  }
}
